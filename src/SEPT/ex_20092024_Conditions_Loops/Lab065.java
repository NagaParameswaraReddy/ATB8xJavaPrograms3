package sept.ex_20092024_Conditions_Loops;

public class Lab065 {
    public static void main(String[] args) {
        // Maximum & minimum between two number by using termery operators.
        int ajai_salary = 4;
        int joseph_salary = 7;
        // How do we find the max & min salary value
        int max = ajai_salary > joseph_salary ? ajai_salary : joseph_salary;
        System.out.println(max);
    }
}
