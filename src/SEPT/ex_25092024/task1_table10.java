package SEPT.ex_25092024;
//Table of number n = 10, print table with For or while.
public class task1_table10 {
    public static void main(String[] args) {
        int a =10;
        for(int i=1;i<=10;i++){
            int t = a*i;
        System.out.println(a+ " * " +i+ "=" +t);
        }
    }
}
