package SEPT.EX_13092024_TASK;

public class TASK_OPERATORS {
    public static void main(String[] args){
        double a=34;
        double b=10;
        System.out.printf("%f+%f=%f \n", a,b,a+b);
        System.out.printf("%f-%f=%f \n", a,b,a-b);
        System.out.printf("%f*%f=%f \n", a,b,a*b);
        System.out.printf("%f/%f=%f \n", a,b,a/b);

    }
}
